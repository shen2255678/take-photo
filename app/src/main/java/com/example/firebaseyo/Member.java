package com.example.firebaseyo;

public class Member {
    private String Name;
    private String Data;
    private String Euid;

    public Member( ) {




    }



    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name =name;
    }

    public String getData() {
        return Data;
    }

    public void setData(String data) {
        Data =data;
    }
    public String getEuid() {
        return Euid;
    }

    public void setEuid(String euid) {
      Euid =euid;
    }

}
